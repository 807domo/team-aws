def apply_fixes():
    """Read seed_data.py and apply text fixes."""
    with open(SEED_DATA_PATH, "r", encoding="utf-8") as f:
        content = f.read()

    fix_count = 0
    for qid, new_text in TEXT_FIXES.items():
        # Find the question's text field in the file using regex
        # Pattern: "id": "qid" ... "text": "old_text"
        # We need to find the text value for this specific question ID
        # The structure is: {"id": "q-xx-nnn", ..., "text": "...", ...}
        
        # Find the question block by ID
        id_pattern = rf'"id":\s*"{re.escape(qid)}"'
        id_match = re.search(id_pattern, content)
        if not id_match:
            print(f"WARNING: Could not find question {qid} in seed_data.py")
            continue
        
        # From the ID position, find the "text" field
        # Look for "text": "..." after the ID within the same dict
        search_start = id_match.start()
        # Find the enclosing dict boundaries (look for the next }, but handle nested)
        # Simple approach: find "text": "..." within ~2000 chars after ID
        search_region = content[search_start:search_start + 3000]
        
        text_pattern = r'"text":\s*"((?:[^"\\]|\\.)*)"'
        text_match = re.search(text_pattern, search_region)
        if not text_match:
            print(f"WARNING: Could not find text field for {qid}")
            continue
        
        old_text = text_match.group(1)
        # Escape the new text for JSON string
        escaped_new_text = new_text.replace("\\", "\\\\").replace('"', '\\"')
        
        # Replace in the actual content
        abs_start = search_start + text_match.start(1)
        abs_end = search_start + text_match.end(1)
        content = content[:abs_start] + escaped_new_text + content[abs_end:]
        fix_count += 1

    # Apply trivia fixes
    trivia_count = 0
    for qid, new_trivia in EHIME_TRIVIA_FIXES.items():
        id_pattern = rf'"id":\s*"{re.escape(qid)}"'
        id_match = re.search(id_pattern, content)
        if not id_match:
            continue
        search_start = id_match.start()
        search_region = content[search_start:search_start + 3000]
        trivia_pattern = r'"ehime_trivia":\s*"((?:[^"\\]|\\.)*)"'
        trivia_match = re.search(trivia_pattern, search_region)
        if not trivia_match:
            continue
        escaped = new_trivia.replace("\\", "\\\\").replace('"', '\\"')
        abs_start = search_start + trivia_match.start(1)
        abs_end = search_start + trivia_match.end(1)
        content = content[:abs_start] + escaped + content[abs_end:]
        trivia_count += 1

    with open(SEED_DATA_PATH, "w", encoding="utf-8") as f:
        f.write(content)

